package com.gyamfimartins.cashback.model;

public class Product {

    String id, url, name, description, terms, current_value;

    public String getId() {
        return id;
    }

    public String getUrl() {
        return url;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public String getTerms() {
        return terms;
    }

    public String getCurrent_value() {
        return current_value;
    }
}
